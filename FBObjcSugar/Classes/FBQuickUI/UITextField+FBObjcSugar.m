#import "UITextField+FBObjcSugar.h"

@implementation UITextField (FBObjcSugar)
+ (instancetype)fb_textFieldWithPlaceHolder:(NSString *)placeHolder {
    
    UITextField *textField = [[self alloc] init];
    
    textField.borderStyle = UITextBorderStyleRoundedRect;
    textField.placeholder = placeHolder;
    
    return textField;
}
@end
